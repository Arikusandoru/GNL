#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (s1[i])
	{
		if (s1[i] == s2[i])
			i++;
		else
			return ((s1[i] - s2[i]) == 0 ? 1 : 0);
	}
	return ((s1[i] - s2[i]) == 0 ? 1 : 0);
}
