#include "libft.h"

void	ft_putnbr(int n)
{
	if (n >= 10 || n <= -10)
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) * (-1 + 2 * (n > 0)) + '0');
	}
	else
	{
		if (n < 0)
			ft_putchar('-');
		ft_putchar((n % 10) * (-1 + 2 * (n > 0)) + '0');
	}
}
